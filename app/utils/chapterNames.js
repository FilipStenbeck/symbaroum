export function chapterNames (number)  {
	switch (number) {
		case 1: return 'Kapitel ett'
		case 2: return 'Kapitel två'
		case 3: return 'Kapitel tre'
		case 4: return 'Kapitel fyra'
		case 5: return 'Kapitel fem'
		case 6: return 'Kapitel sex'
		case 7: return 'Kapitel sju'
		case 8: return 'Kapitel åtta'
		case 9: return 'Kapitel nio'
		default: return number
	}
}
